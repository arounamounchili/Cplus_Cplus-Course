#include <iostream>
#include <string> // Include the string library

using namespace std;

int main()
{
    /* Numeric Data Types: int, float, double */
    int studentNumber = 130;
    float size = 1.4;
    double price = 200.44;

    // Scientific Numbers
    float number1 = 12e2;
    double number2 = 23E3;

    /* Boolean data types: bool */
    bool isMajor = true;
    bool isMonday = false;

    /* Character Data Types: char */
    char letterA = 'a';
    char letterB = 66; // You can use ASCII values too

    /* String Data types: string */
    string greeting = "Hallo";
    string texte = "Salut, comment allez-vous?";
    cout << texte << endl;

    return 0;
}
